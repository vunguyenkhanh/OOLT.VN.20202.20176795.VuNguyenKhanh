package lab02;

import java.awt.*;
import java.applet.*;

/**
 * A simple circle class.
 *
 * @author Samuel A.Rebelsky
 * @version 1.1 of February 1999
 */
public class Circle {
    /** The current color of the circle.*/
    protected  Color color;

    /** The current diameter of the circle.*/
    protected int diameter;

    /**
     *Create a new circle with specified diameter and clolor
     */
    public Circle(int diameter, Color color){
        this.diameter = diameter;
        this.setColor(color);
    }

    /**
     *Create a new circle with specified diameter and clolor
     */
    public Circle(int diameter){
        this.diameter = diameter;
        this.setColor(Color.blue); //blus is as good a default as any.
    }

}
